numero1 = float(input("Insira o primeiro numero:"))
numero2 = float(input("Insira o segundo numero:"))

if numero1 > numero2:
    print(f"O numero {numero1} é maior que o numero {numero2}.")

elif numero1 < numero2:
    print (f"O numero {numero1} é menor que o numero {numero2}")

else:
    print(f"O numero {numero1} é igual ao numero {numero2}.")